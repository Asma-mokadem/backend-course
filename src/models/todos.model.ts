import mongoose from "mongoose";

const todoSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      default: "",
    },
    status: {
      type: String,
      enum: ["en cours", "en attente", "terminé"],
      default: "en attente",
    },
    priority: {
      type: String,
      enum: ["low", "medium", "high"],
      default: "medium",
    },
    category: {
      type: String,
      default: "Général",
    },
  },
  { timestamps: true }
);

export const TodoModel = mongoose.model("Todo", todoSchema);